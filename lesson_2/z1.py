print("Данная программ произведет арифметические действия с двумя числами.")
a=float(input('Введите первое число: '))
b=float(input('Введите второе число: '))
temp=a+b
print(a,'+',b,'=',temp)
temp=a-b
print(a,'-',b,'=',temp)
temp=a*b
print(a,'*',b,'=',temp)
temp=a/b
print(a,'/',b,'=',temp)
temp=a**b
print(a,'**',b,'=',temp)
temp=a%b
print(a,'%',b,'=',temp)
temp=a//b
print(a,'//',b,'=',temp)